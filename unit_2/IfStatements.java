public class IfStatements {
    public static void main(String[] args) {
        int rollNum = 5;
        
        if(rollNum == 1){
            System.out.println("Ram Kumar");
        }else if(rollNum == 2){
            System.out.println("Shyam Prashad");
        }else if(rollNum == 3){
            System.out.println("Geeta Kumari");
        }else{
            System.out.println("Not Authorized!");
        }
    }
}
