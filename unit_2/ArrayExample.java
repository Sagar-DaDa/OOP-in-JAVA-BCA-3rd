package unit_2;

public class ArrayExample {
    public static void main(String[] args) {
        int[] ages = {18, 25, 30, 35};

        System.out.println("First element: " + ages[0]); // Accessing the first element
        ages[2] = 40; // Modifying an element
        System.out.println("Modified third element: " + ages[2]);
    }
}
