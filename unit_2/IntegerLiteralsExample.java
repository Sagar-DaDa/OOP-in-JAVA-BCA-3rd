package unit_2;

public class IntegerLiteralsExample {
    public static void main(String[] args) {
        int decVal = 26;    // Decimal
        int octVal = 067;   // Octal
        int hexVal = 0x1a;  // Hexadecimal
        int binVal = 0b11010; // Binary

        System.out.println("Decimal: " + decVal);
        System.out.println("Octal: " + octVal);
        System.out.println("Hexadecimal: " + hexVal);
        System.out.println("Binary: " + binVal);
    }
}

